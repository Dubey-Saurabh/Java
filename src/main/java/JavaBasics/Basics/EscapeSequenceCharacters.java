package JavaBasics.Basics;

public class EscapeSequenceCharacters {

    public static void main(String[] args) {

        //To provide quotes to a string
        System.out.println("\"Saurabh is my name\"");

        ////To provide a backspace to a string
        System.out.print("Description: :");
        System.out.print("\bSaurabh is my name");




    }
}
