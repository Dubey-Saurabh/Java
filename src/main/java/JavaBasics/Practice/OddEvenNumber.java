package JavaBasics.Practice;

public class OddEvenNumber {

    public static void OddEven(int num){

        if (num%2==0){
            System.out.println("No is even: 1");
        }else{
            System.out.println("No is odd: 0");
        }

    }

    public static void main(String[] args) {

        OddEven(231);

    }

}
